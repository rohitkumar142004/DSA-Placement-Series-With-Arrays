package Basicofjava;

public class ArithmeticOperator {
    static void main(String[] args) {
        System.out.println(10 + 2);
        System.out.println(10 - 2);
        System.out.println(10 * 2);
        System.out.println(10 / 2);

        int x = 5, y = 3;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);   // decimal is ignored  because we are doing operator between two integer
    }
}



