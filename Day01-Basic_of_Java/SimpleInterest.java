package Basicofjava;

public class SimpleInterest {
    static void main(String[] args) {
        double Principal = 188.97;
        double rate = 2.34;
        double time = 3;
        double SI = (Principal * rate * time )/100;
        System.out.println("The Simple Interest is: " + SI);
    }
}

