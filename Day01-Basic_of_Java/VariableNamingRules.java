package Basicofjava;

public class VariableNamingRules {
    static void main(String[] args) {
        int x1 = 4; // Right
        // int 1x = 4 Wrong
        // variable can not be started with number it can be started with any alphabet and underscore _ and $
        // variables cannot contain special characters  !
        // int x! x& x they are not allowed with the variable name
        // ! @ # % ^ & * - = ( ) { } [ ] \ | ' ; " : / ? . > , <

        int x = 4;
        // spaces are not allowed
        // keyword are not allowed
       //  int double = 4  this can not be possible
        // int class = 4 this is also not possible because this is a reserve keywords

        int _x$ = 5;
        int INT = 5;
        int Int = 6;

        // keywords are always in lowercase so we can use it using uppercase

        // 3 4 5 6 8 9 11  13

    }
}
