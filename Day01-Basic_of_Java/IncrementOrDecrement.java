package Basicofjava;
import java.util.Scanner;
public class IncrementOrDecrement {
    // ++x, x++, --x, x--
    static void main(String[] args) {
        int x = 6;
        x += 7; // x = x + 7
        System.out.println(x);
        x -= 3;
        System.out.println(x);
        x *= 3;
        System.out.println(x);

    }

}
